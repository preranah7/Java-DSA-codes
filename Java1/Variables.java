public class Variables {
    public static void main(String args[]){
        byte b = 8;//data type byte(combination of 8 bits)
        System.out.println(b);
        char ch = 'a'; //only single characters and single quotes are allowed in char datatype (size 2 bytes)
        System.out.println(ch);
        boolean var = true;//or false (size 1 byte)
        System.out.println(var);//data type var
        float price = 9;//Decimal number(size 4 bytes)
        System.out.println(price);//data type float
        int num=89;//(size 4 bytes)
        System.out.println(num);//data type int
        short n = 240;//(size 2 bytes)
        System.out.println(n);//data type short
        //long(size 8 bytes)
        //double(size 8 bytes)
        //Value of variables can vary in the same source code

    }
}
